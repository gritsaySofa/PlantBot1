namespace TelegramPlantBot.Services
{
    public class DiagnosisService
    {
        public static string DiagnosePlantProblem(string symptoms)
        {
            symptoms = symptoms.ToLower();

            if (symptoms.Contains("желтеют") && symptoms.Contains("листья"))
            {
                return "🔍 **Желтеют листья**\n\n" +
                       "Возможные причины:\n" +
                       "• 💧 Перелив или недостаточный дренаж\n" +
                       "• 🌿 Недостаток питательных веществ (азот)\n" +
                       "• 💨 Слишком сухой воздух\n" +
                       "• ☀️ Избыток прямого солнечного света\n\n" +
                       "**Рекомендации:**\n" +
                       "• Проверьте влажность почвы\n" +
                       "• Удобрите растение\n" +
                       "• Увеличьте влажность воздуха";
            }

            if (symptoms.Contains("сохнут") && symptoms.Contains("кончики"))
            {
                return "🔍 **Сохнут кончики листьев**\n\n" +
                       "Возможные причины:\n" +
                       "• 💨 Слишком сухой воздух\n" +
                       "• 💧 Недостаточный полив\n" +
                       "• 🌿 Избыток удобрений\n" +
                       "• 🚰 Использование жесткой воды\n\n" +
                       "**Рекомендации:**\n" +
                       "• Увеличьте влажность\n" +
                       "• Отстаивайте воду для полива\n" +
                       "• Проверьте график подкормок";
            }

            if (symptoms.Contains("опадают") || symptoms.Contains("сбрасывает"))
            {
                return "🔍 **Опадают листья**\n\n" +
                       "Возможные причины:\n" +
                       "• 💨 Сквозняки или резкие перепады температуры\n" +
                       "• 💧 Перелив или пересушка\n" +
                       "• 🔄 Смена местоположения\n" +
                       "• 🌱 Естественный процесс у некоторых растений\n\n" +
                       "**Рекомендации:**\n" +
                       "• Устраните сквозняки\n" +
                       "• Стабилизируйте условия\n" +
                       "• Дайте растению адаптироваться";
            }

            return "🔍 **Общие рекомендации:**\n\n" +
                   "• Проверьте влажность почвы\n" +
                   "• Осмотрите растение на наличие вредителей\n" +
                   "• Обеспечьте правильное освещение\n" +
                   "• Проверьте температуру и влажность\n" +
                   "• Убедитесь в отсутствии сквозняков\n\n" +
                   "Для точной диагностики опишите симптомы подробнее или добавьте фото";
        }
    }
}